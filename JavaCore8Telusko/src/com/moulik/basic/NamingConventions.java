package com.moulik.basic;

public class NamingConventions {

	public static void main(String[] args) {
		
		/*
		 * Java recommends some practices for making code more readable and efficient:
		 *
		 * Variable names should be something like sName, stockPrice etc
		 * Class, Interface names should have first letter caps
		 * Packages names should be all small_case
		 * Constants names should be all caps like PI, DENSITY, MAX_PRICE etc
		 * 
		 * 
		 * Class names should be like nouns like Student, Integer, Engineer etc
		 * Interfaces should be like adjectives like Runnable, Serializable etc
		 * Method names should be like actions like run, actionPerformed, print etc
		 * 
		 */

	}

}
